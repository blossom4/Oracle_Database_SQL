-- DDL (Data Definition Language)
-- It manages the structure of tables and indexes.
-- create, drop, alter ...

-- DML (Data Manipulation Language)
-- It stores, modifies, deletes, and extracts data on tables.
-- insert, update, delete, select ...

-- DCL (Data Control Language)
-- It can handle user permissions, cancellation of tasks.
-- grant, revoke, commit, rollback ...